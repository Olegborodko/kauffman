<script>

    $(function() {
        $('.people').flip();

        $(window).scroll(function(){
            if($(this).scrollTop() > 50) {
                $('.fixed-top ').addClass('bg');
            }else{
                $('.fixed-top ').removeClass('bg');
            }
        });


            var w        = $(window),
                h        = $("#header"),
                pos      = 0;
            tmp      = 0;
            onScroll = function() {

                // Detect Mobile Width
                if(w.width() < 941) {
                    h.addClass('mobile');
                    h.removeClass('scrolled');
                    return;
                } else {
                    h.removeClass('mobile');
                }

                // onScroll fires onload multiple times depending on intertia
                // scrolling and browsers. To counter this and make sure the
                // navigation can show at the beginning of each load
                // we will add a tmp variable that will have to fire 10 times
                // before it can get to a scroll up event.
                if(tmp < 10) {
                    h.addClass('scrolled down');
                    tmp++;
                    return;
                }

                h[w.scrollTop() > 70 ? "addClass" : "removeClass"]('scrolled');

                //scroll down
                if(w.scrollTop() < pos) {
                    h.addClass('down');
                } else {
                    h.removeClass('down');
                }
                pos = w.scrollTop();
            };
            $(window).on('scroll', onScroll);
            $(window).on('resize', onScroll);
            onScroll();

    });
</script>
